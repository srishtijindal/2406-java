public class Initials {

    public static void main(String[] args) {
        char MYFIRST = 'J', MYMIDDLE = 'M', MYLAST = 'F';
        DisplayInitials(MYFIRST, MYMIDDLE, MYLAST);

    }
    public static void DisplayInitials(char first, char middle, char last)
    {

        System.out.print("The First Initial is ");
        System.out.println(first);
        System.out.print("The Middle Initial is ");
        System.out.println(middle);
        System.out.print("The Last Initial is ");
        System.out.println(last);

    }
}








